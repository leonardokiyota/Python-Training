'''
          Faça um programa que peça dois números inteiros. Imprima a soma
          desses dois na tela
'''
num1 = int(input("Digite um número: "))
num2 = int(input("Digite outro número: "))

print("A soma do número %d mais %d é %d." %(num1, num2, num1 + num2))
